package jadex.android.applications.demos.rest;

public class DataItem
{
	private String label;
	private int color;
	private int[] data;
	
	public String getLabel()
	{
		return label;
	}
	public void setLabel(String label)
	{
		this.label = label;
	}
	public int getColor()
	{
		return color;
	}
	public void setColor(int color)
	{
		this.color = color;
	}
	public int[] getData()
	{
		return data;
	}
	public void setData(int[] data)
	{
		this.data = data;
	}
	
	
}
